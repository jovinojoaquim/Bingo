    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            jogadores = Integer.parseInt(jTextField2.getText());
            while (jogadores <= 1 || jogadores >= 11) {
                JOptionPane.showMessageDialog(rootPane, "Digite entre 2 e 10 jogadores");
                return;
            }
            new Thread(new Inicio(jogadores)).start();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite Apenas n√∫meros inteiros");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

